/*
 * related
 * - data stored in a database cannot exist in isolation, it must have a relationship with other data
 * 
 * primary key field
 * - it contains unique values that cannot be replicated elsewhere in the table
 * 
 * foreign key field
 * - is a field in one table that connects to the primary key field in the original table
 * 
 * relationship
 * - the primary key of the table, but it becomes a foreign key of another table, the relationship is established and the data in these two table are related
*/